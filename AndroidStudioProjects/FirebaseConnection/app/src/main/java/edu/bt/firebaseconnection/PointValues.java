package edu.bt.firebaseconnection;

public class PointValues {
    String xValues;
    int yValues;

    public PointValues() {
    }

    public PointValues(String xValues, int yValues) {
        this.xValues = xValues;
        this.yValues = yValues;
    }

    public String getxValues() {
        return xValues;
    }

    public int getyValues() {
        return yValues;
    }
}
